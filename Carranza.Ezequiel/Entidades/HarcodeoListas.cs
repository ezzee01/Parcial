        /// <summary>
        /// Asigno Un responsable a cada alumno
        /// </summary>
        /// <param name="alumnos">Lista de alumnos</param>
        /// <param name="responsables">Lista de responsables</param>
        public static void AgregarResponsableEnAlumno(List<Alumno> alumnos, List<Responsable> responsables)
        {
            for (int i = 0; i < alumnos.Count; i++)
            {
                alumnos.ElementAt<Alumno>(i).Responsable = responsables.ElementAt<Responsable>(i);
            }
        }


